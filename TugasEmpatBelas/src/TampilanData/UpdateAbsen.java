    private void idkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idkActionPerformed
        String selectedId = (String) idk.getSelectedItem();
        if (selectedId == null) {
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
        EntityManager em = emf.createEntityManager();

        try {
            Karyawan k = em.find(Karyawan.class, selectedId);
            if (k == null) {
                JOptionPane.showMessageDialog(this,
                        "ID Karyawan tidak ditemukan di database!",
                        "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_idkActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String oldId = ida.getName();
        String newId = ida.getText().trim();

        if (newId.isEmpty()
                || idk.getSelectedItem() == null
                || tanggal.getDate() == null
                || masuk.getValue() == null
                || keluar.getValue() == null) {

            JOptionPane.showMessageDialog(this,
                    "Semua field harus diisi!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();

            Absensi cek = em.find(Absensi.class, newId);
            if (cek != null && !newId.equals(oldId)) {
                JOptionPane.showMessageDialog(this,
                        "ID Absensi sudah digunakan, pilih ID lain!",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            Absensi absenLama = em.find(Absensi.class, oldId);
            if (absenLama == null) {
                JOptionPane.showMessageDialog(this,
                        "ID Absensi lama tidak ditemukan!",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            String idKaryawanStr = (String) idk.getSelectedItem();
            Karyawan k = em.find(Karyawan.class, idKaryawanStr);
            if (k == null) {
                JOptionPane.showMessageDialog(this,
                        "ID Karyawan tidak ditemukan!",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            em.remove(absenLama);
            em.flush();

            Absensi absenBaru = new Absensi();
            absenBaru.setIdabsensi(newId);
            absenBaru.setIdkaryawan(k);
            absenBaru.setTanggal(new java.sql.Date(tanggal.getDate().getTime()));
            absenBaru.setJamMasuk(new java.sql.Time(((Date) masuk.getValue()).getTime()));
            absenBaru.setJamKeluar(new java.sql.Time(((Date) keluar.getValue()).getTime()));

            em.persist(absenBaru);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this,
                    "Data absensi berhasil diupdate!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);

            if (parentFrame != null) {
                parentFrame.tampilkanDataAbsensi();
            }

            dispose();

        } catch (Exception e) {
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan saat update: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_updateActionPerformed

