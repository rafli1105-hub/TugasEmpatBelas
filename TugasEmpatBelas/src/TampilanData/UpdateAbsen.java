    private void loadKaryawanToComboBox(String selectedId) {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
        EntityManager em = emf.createEntityManager();

        try {
            List<Karyawan> listKaryawan = em.createQuery("SELECT k FROM Karyawan k", Karyawan.class).getResultList();
            idk.removeAllItems();
            for (Karyawan k : listKaryawan) {
                idk.addItem(k.getIdkaryawan());
            }

            if (selectedId != null) {
                idk.setSelectedItem(selectedId);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data karyawan: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }

