    private void cekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekActionPerformed
    if (jawaban.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Jawaban tidak boleh kosong!");
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
        EntityManager em = emf.createEntityManager();

        Pw userr = em.find(Pw.class, user.getText());

        if (userr == null) {
            JOptionPane.showMessageDialog(this, "User tidak ditemukan!");
            em.close();
            emf.close();
            return;
        }

        String inputJawaban = jawaban.getText().trim();
        String jawabanBenar = userr.getJawabanBenar().trim();

        if (inputJawaban.equalsIgnoreCase(jawabanBenar)) {
            JOptionPane.showMessageDialog(this, "Jawaban benar! Silakan buat password baru.");

            pass1.setVisible(true);
            pass.setVisible(true);
            ubah.setVisible(true);

            pass.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Jawaban salah!");
            jawaban.setText("");
            jawaban.requestFocus();
        }

        em.close();
        emf.close();
    }//GEN-LAST:event_cekActionPerformed

