    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        if (user.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi Terlebih Dahulu");
        } else {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
            EntityManager em = emf.createEntityManager();

            em.getTransaction().begin();

            String user1 = user.getText();
            Pw y = em.find(Pw.class, user1);

            if (y == null) {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                user.requestFocus();
            } else {
                lblsoal.setText(y.getPertanyaan());
                lblsoal.setVisible(true);
                jawab1.setVisible(true);
                jawaban.setVisible(true);
                cek.setVisible(true);
                user.setEditable(false);
                jawaban.requestFocus();
            }
            em.getTransaction().commit();
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_cariActionPerformed

    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        if (pass.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password baru tidak boleh kosong!");
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
        EntityManager em = emf.createEntityManager();

        em.getTransaction().begin();

        Pw akun = em.find(Pw.class, user.getText());

        akun.setPassword(pass.getText()); 

        em.getTransaction().commit();

        JOptionPane.showMessageDialog(this, "Password berhasil diubah!");

        em.close();
        emf.close();

        Login masuk= new Login();
        masuk.setVisible(true);
        dispose();
        
    }//GEN-LAST:event_ubahActionPerformed

