    private void loadKaryawanToComboBox() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
        EntityManager em = emf.createEntityManager();

        try {
            List<Karyawan> listKaryawan = em.createQuery("SELECT n FROM Karyawan n", Karyawan.class).getResultList();
            IDK.removeAllItems();

            for (Karyawan n : listKaryawan) {
                IDK.addItem(n.getIdkaryawan());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data karyawan: " + e.getMessage());
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }

