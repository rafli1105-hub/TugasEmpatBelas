    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
            EntityManager em = emf.createEntityManager();

            String idAbsensi = IDA.getText().trim();
            String idKaryawan = (String) IDK.getSelectedItem();
            java.util.Date tanggal = tgl.getDate();

            java.util.Date jamMasukUtil = (java.util.Date) masuk.getValue();
            java.util.Date jamKeluarUtil = (java.util.Date) keluar.getValue();

            java.sql.Time jamMasuk = new java.sql.Time(jamMasukUtil.getTime());
            java.sql.Time jamKeluar = new java.sql.Time(jamKeluarUtil.getTime());

            if (idAbsensi.isEmpty() || idKaryawan == null || tanggal == null) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Absensi a = new Absensi();
            a.setIdabsensi(idAbsensi);
            a.setTanggal(new java.sql.Date(tanggal.getTime()));
            a.setJamMasuk(jamMasuk);
            a.setJamKeluar(jamKeluar);

            Karyawan k = em.find(Karyawan.class, idKaryawan);
            if (k == null) {
                JOptionPane.showMessageDialog(this, "ID Karyawan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            a.setIdkaryawan(k);

            em.getTransaction().begin();
            em.persist(a);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data absensi berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            if (parentFrame != null) {
                parentFrame.tampilkanDataAbsensi();
            }

            dispose();

            em.close();
            emf.close();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void IDKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IDKActionPerformed
        String selectedId = (String) IDK.getSelectedItem();
        if (selectedId == null) {
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HariKelimaPU");
        EntityManager em = emf.createEntityManager();

        try {
            Karyawan k = em.find(Karyawan.class, selectedId);
            if (k == null) {
                JOptionPane.showMessageDialog(this,
                        "ID Karyawan tidak ditemukan di database!",
                        "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_IDKActionPerformed

